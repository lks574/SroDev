import AsyncDisplayKit

extension UIColor {
    
    static var valid: UIColor {
        return UIColor.init(red: 0.2, green: 0.8, blue: 0.2, alpha: 1.0)
    }
    
    static var invalid: UIColor {
        return UIColor.init(red: 0.8, green: 0.2, blue: 0.3, alpha: 1.0)
    }
}
